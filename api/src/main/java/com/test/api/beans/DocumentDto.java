package com.test.api.beans;

public class DocumentDto {

    private String id;
    private String text;

    public DocumentDto() {}

    public DocumentDto(String id, String text) {
        this.id = id;
        this.text = text;
    }

    public String getId() {
        return id;
    }

    public String getText() {
        return text;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setText(String text) {
        this.text = text;
    }
}
